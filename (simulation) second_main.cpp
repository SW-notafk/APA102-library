///This function gets the parameters passed to the "UpdateFrame" function or "DisplayText"and is used to read and send particular places in the array.
void LedStrip::SendColor(char r, char g, char b){
	LedTransmit(224 + global_brightness); // 3 up-front bites and brightness of the LED
	LedTransmit(r); // red color specification range: 0 - 255
	LedTransmit(g); // green color specification range: 0 - 255
	LedTransmit(b); // blue color specification range: 0 - 255
}

