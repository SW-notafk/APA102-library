///Initialize the SPI for the communication between the micro-controller and the LED strip.
void LedStrip::LedInit(){
	///Setting clock and Master Out Slave In.
	SPI_DDR |= (1<<SS) | (1<<MOSI) | (1<<SCK);
	///Enabling SPI, specifying master and slave, setting the clock frequency.
	SPCR = (1<<SPE) | (1<<MSTR) | (1<<SPR0);	
}
///Transmitting data to the LED strip
void LedStrip::LedTransmit(uint8_t data){
	///Put data to the SPI Data Register
	SPDR = data;
	///Wait for the data to be sent
	while (!(SPSR & (1 << SPIF)));
}
///Set several LEDs in a row for the same color and brightness. 
///This function takes amount of LEDs that you want to use, brightness for them and "rgb" parameters to set the color. 
void LedStrip::SetSeveralAndSend(uint8_t LedAmount, uint8_t brightness, uint8_t blue,uint8_t green, uint8_t red){
	SPI_DDR &= ~(1 << SS);
	//starting frame
	LedTransmit(0);
	LedTransmit(0);
	LedTransmit(0);
	LedTransmit(0);
	
	
	/*
	This part has to be done in order to light up all LEDs that were entered as LedAmount in the function	
	*/
	//LED frame
	
	if (LedAmount > 24 && LedAmount < 35){
		LedAmount++;
	}
	if (LedAmount > 35 && LedAmount < 44){
		LedAmount += 2;
	}
	if (LedAmount > 44 && LedAmount < 53){
		LedAmount += 3;
	} 
	if (LedAmount > 53 && LedAmount < 62){
		LedAmount += 4;
	}
	if (LedAmount > 62){
		LedAmount += 5;
	}
	
	for (int i = 0; i <= LedAmount; i++){
		LedTransmit(224 + brightness); // 224 (1110 0000) is sent as 3 necessary bits, brightness should range from 0 - 31
		LedTransmit(blue);
		LedTransmit(green);
		LedTransmit(red);
	}
	
	//Ending frame 
	// 0 worked for sure
	LedTransmit(255);
	LedTransmit(255);
	LedTransmit(255);
	LedTransmit(255);
	SPI_DDR |= (1 << SS);	
}
///Function for creating only a starting frame, meant to be used with other "create" functions.
void LedStrip::CreateStartFrame(){
	SPI_DDR &= ~(1 << SS);
	//Starting frame
	LedTransmit(0);
	LedTransmit(0);
	LedTransmit(0);
	LedTransmit(0);
}
///Function that creates LED frame, this function takes amount of LEDs that the user want to use, brightness of those LEDs the rgb parameters.
void LedStrip::CreateLedFrame(uint8_t LedAmount, uint8_t brightness, uint8_t blue,uint8_t green, uint8_t red){
	//LED frame
	for (int i = 0; i < LedAmount; i++){
		LedTransmit(224 + brightness); // 224 (1110 0000) is sent as 3 necessary bits, brightness should range from 0 - 31
		LedTransmit(blue);
		LedTransmit(green);
		LedTransmit(red);
	}
}
///Function that creates a "blank" LED - LED that is switched off
void LedStrip::CreateLedBlankFrame(uint8_t LedAmount){
	//LED blank frame
	for (int i = 0; i < LedAmount; i++){
		LedTransmit(224 + 31); // 224 (1110 0000) is sent as 3 necessary bits, brightness should range from 0 - 31
		LedTransmit(0);
		LedTransmit(0);
		LedTransmit(0);
	}	
}
///Function that creates an ending frame, meant to be used with other "create" functions.
void LedStrip::CreateEndFrame(){
	//Ending frame
	LedTransmit(255);
	LedTransmit(255);
	LedTransmit(255);
	LedTransmit(255);
	SPI_DDR |= (1 << SS);
}
